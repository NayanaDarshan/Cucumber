public class Search {

	
	public String getdisplayedProductName(Product product) {
		
		if(product.equals(product.getProductList().contains(product.getProductName()))){
			System.out.println(" The searched product is displayed" +product.getProductName());
		}
		else {
			System.out.println("The searched Product is not displayed");
		}
	return product.getProductName();
	}
	

}
